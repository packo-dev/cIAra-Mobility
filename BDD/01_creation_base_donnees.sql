-- Suppression des tables existantes (si elles existent)
DROP TABLE IF EXISTS location;
DROP TABLE IF EXISTS vehicule;
DROP TABLE IF EXISTS client;
DROP TABLE IF EXISTS station;

-- Table station
CREATE TABLE station (
    id_station SERIAL PRIMARY KEY,
    nom VARCHAR(100) NOT NULL,
    ville VARCHAR(50) NOT NULL
);

-- Table vehicule
CREATE TABLE vehicule (
    id_vehicule SERIAL PRIMARY KEY,
    marque VARCHAR(50) NOT NULL,
    modele VARCHAR(50) NOT NULL,
    annee INT NOT NULL,
    energie VARCHAR(20) NOT NULL,
    autonomie_km INT NOT NULL,
    immatriculation VARCHAR(20) NOT NULL UNIQUE,
    etat VARCHAR(20) NOT NULL,
    localisation VARCHAR(50) NOT NULL
);

-- Table client
CREATE TABLE client (
    id_client SERIAL PRIMARY KEY,
    nom VARCHAR(50) NOT NULL,
    prenom VARCHAR(50) NOT NULL,
    email VARCHAR(100) NOT NULL UNIQUE
);

-- Table location
CREATE TABLE location (
    id_location SERIAL PRIMARY KEY,
    date_debut DATE NOT NULL,
    date_fin DATE,
    id_client INT NOT NULL,
    id_vehicule INT NOT NULL,
    id_station_depart INT NOT NULL,
    id_station_arrivee INT,
    FOREIGN KEY (id_client) REFERENCES client(id_client),
    FOREIGN KEY (id_vehicule) REFERENCES vehicule(id_vehicule),
    FOREIGN KEY (id_station_depart) REFERENCES station(id_station),
    FOREIGN KEY (id_station_arrivee) REFERENCES station(id_station)
);

